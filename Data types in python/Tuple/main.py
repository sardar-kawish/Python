# 1. **len(tuple)**: It calculates the length of the tuple.

tuple = (1, 2, 3, 4, 5)
length = len(tuple)
print(length)  # Output: 5

# 2. **max(tuple)**: It returns the maximum element of the tuple.

tuple = (1, 2, 3, 4, 5)
max_value = max(tuple)
print(max_value)  # Output: 5

# 3. **min(tuple)**: It returns the minimum element of the tuple.

tuple = (1, 2, 3, 4, 5)
min_value = min(tuple)
print(min_value)  # Output: 1

# 4. **tuple(seq)**: It converts the specified sequence (e.g., a list) to a tuple.

list = [1, 2, 3, 4, 5]
tuple = tuple(list)
print(tuple)  # Output: (1, 2, 3, 4, 5)
